// Operadores Relacionales

let a = 10;
console.log(a > 5); // True
console.log(a >= 5); // True
console.log(a < 5); // False
console.log(a <= 5); // False

// Operadores de Igualdad
console.log(a == 10); // Nos devuelve True
console.log(a != 10); // Nos devuelve False
console.log(a === '10'); //Consulta si tiene el mismo valor
console.log(a !== '10'); // Pregunta si son distintos tipos de datos